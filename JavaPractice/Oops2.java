public class Oops2 {
    public static void main(String[] args) {
        int x = 0;
        System.out.println("x is "+x);

        x = (int)15.2;   // set x to 15.2
        System.out.printf("x is now "+x);
        System.out.printf("\n");
        // set y to 1 more than x
        //x=15;
        int y = x + 1;
        System.out.println("x and y are " + x +" and " + y);
    }
    }
